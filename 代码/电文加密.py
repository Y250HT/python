# 电文加密
# 有一行电文，已按如下规律译成密码：
# A-->Z a-->z
# B-->Y b-->y
# C-->X c-->x
# ...... ......
# 即第一个字母变成第26个字母，第i个字母变成第（26-i+1)个字母，非字母字符
# 不变。要求根据密码译回原文，并输出。

s = input()
t = ""
for c in s:
    if 'a' <= c <= 'z':
        t += chr(122 - ord(c) + 97)
        #a->97 z->122
        #ord() 返回对应的ASCII数值 返回值是对应的十进制整数
    elif 'A' <= c <= 'Z':
        t += chr(90 - ord(c) + 65)
        #chr()返回值是当前整数对应的ASCII字符
        #A->65  Z->90
    else:
        t+=c
print(t)
#本程序采用ASCII码进行编写